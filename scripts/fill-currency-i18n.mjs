for (const lang of Object.keys(currencies)) {
  const sharedPath = path.join(root, lang, "shared.json");
  const shared = JSON.parse(fs.readFileSync(sharedPath, "utf8"));
  shared.currencies = { ...(shared.currencies || {}), ...currencies[lang] };
  shared.currencyTicker = { ...(shared.currencyTicker || {}), ...ticker[lang] };
  if (shared.priceCurrencySelect) {
    shared.priceCurrencySelect.ariaLabel = priceCurrencyAria[lang];
  }
  fs.writeFileSync(sharedPath, `${JSON.stringify(shared, null, 2)}\n`, "utf8");
  console.log("shared", lang);

  const exchangePath = path.join(root, lang, "exchange.json");
  if (fs.existsSync(exchangePath)) {
    const ex = JSON.parse(fs.readFileSync(exchangePath, "utf8"));
    ex.currencies = { ...(ex.currencies || {}), IRR: irrLabel[lang] };
    // Keep apiKeys structure from fa if scaffold emptied them
    const faEx = JSON.parse(fs.readFileSync(path.join(root, "fa", "exchange.json"), "utf8"));
    if (!Array.isArray(ex.apiKeys) || !ex.apiKeys[0]?.id) {
      ex.apiKeys = faEx.apiKeys;
    }
    fs.writeFileSync(exchangePath, `${JSON.stringify(ex, null, 2)}\n`, "utf8");
    console.log("exchange", lang);
  }
}

